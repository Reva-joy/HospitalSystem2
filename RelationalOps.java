public class RelationalOps {
    public static void main(String[] args) {

        int val1 = 50;
        int val2 = 30;

        System.out.println("Is val1 greater than val2? " + (val1 > val2));
        System.out.println("Is val1 less than val2? " + (val1 < val2));
        System.out.println("Is val1 equal to val2? " + (val1 == val2));
        System.out.println("Is val1 not equal to val2? " + (val1 != val2));
        System.out.println("Is val1 greater than or equal to val2? " + (val1 >= val2));
        System.out.println("Is val1 less than or equal to val2? " + (val1 <= val2));
    }
}
